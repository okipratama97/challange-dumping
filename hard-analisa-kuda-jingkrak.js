/*
R = Ratu
K = Kuda

Cari tau apakah Kuda bisa memakan Ratu
*/

function chess(catur) {
// your code here
    let posisiK = []
    let posisiR = []
    for (let i = 0; i < catur.length; i++){
        for (let j = 0; j < catur[i].length; j++){
            if (catur[i][j] === "K"){
                posisiK.push(i)
                posisiK.push(j)
            }else if (catur[i][j] === "R"){
                posisiR.push(i)
                posisiR.push(j)
            }
        }
    }
    distance = Math.abs(posisiK[0] - posisiR[0]) + Math.abs(posisiK[1] - posisiR[1])
    if (distance !== 3){
        return "gagal"
    }
    if (posisiK[0] === posisiR[0] || posisiK[1] === posisiR[1]){
        return "gagal"
    }
    return "berhasil"
}

